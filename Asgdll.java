public class Asgdll {
	
	//method main lokasi program akan di eksekusi
	public static void main(String[] args) 
	{
		/* deklarasi variabel f dan f11.
			 variabel f telah diberi nilai 20.0. 
			note: untuk variabel float setelah penulisan nilai variabel
				diberi tambahan huruf f.
		*/
		float f = 20.0f;
		double f11;
		
		//variabel f11 diberi nilai
		f11=10.0;
		
		/*menampilkan isi dari f dan f11
		  \n berfungsi untuk membuat baris baru.
		*/
		
		System.out.println("f : "+f +"\nf11 : "+f11);
	}
}